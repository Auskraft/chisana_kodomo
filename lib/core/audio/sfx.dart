/// Короткие звуковые события (детский набор). Имя — событие, не файл: бэкенд
/// подключим в Фазе 5 (процедурные джинглы / CC0), сейчас контракт уже стабилен.
enum SfxEvent {
  /// Касание объекта/кнопки.
  tap,

  /// Правильно — мягкий радостный «дзынь».
  correct,

  /// Не то — нейтральный мягкий «пуф» (без негатива, «попробуй ещё»).
  soft,

  /// Получена звезда.
  star,

  /// Набор пройден — маленький фанфар.
  complete,

  /// Старт игры.
  start,
}

